class Pet :
    def __init__(self, id, name, species, breed, sex, age, weight, image, documents, contact):
        self.id = id
        self.name = name
        self.species = species
        self.breed = breed
        self.sex = sex
        self.age = age
        self.weight = weight
        self.image = image
        self.documents = documents
        self.contact = contact